export class Character {
	constructor(
		id,
		name,
		status,
		species,
		type,
		gender,
		origin,
		location,
		image,
		episode,
		url,
		created
	) {
		this.id = id;
		this.name = name;
		this.status = status;
		this.species = species;
		this.type = type;
		this.gender = gender;
		this.origin = origin;
		this.location = location;
		this.image = image;
		this.episode = episode;
		this.url = url;
		this.created = created;
	}
}
